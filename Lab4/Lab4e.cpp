#include <iostream>

#include <cmath>

using namespace std;

 

int main () {

  bool b1, b2; // These variables will store the results of relational expressions

  float  x, y; // These variables will store user input

   

  /* Get user input */

  cout << "Enter two numeric values x and y: ";

  cin >> x >> y;

   

  /* Set values of b1 and b2 based on values of x and y */

  b1 = x == y;

  b2 = x <= sqrt(y);

 

  /* Print b1 and b2 */

  cout << "b1 = " << b1 << endl;

  cout << "b2 = " << b2 << endl;

  return 0;

}
